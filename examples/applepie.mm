      Title: Dutch Apple Pie
 Categories: Pies
      Yield: 8 servings

      1 ea Graham Cracker ready crust
      1 ea large egg yolk,slightly beat
    1/4 t  salt
    1/2 t  ground cinnamon
    1/4 t  ground nutmeg
    3/4 c  all-purpose flour
  5 1/2 c  peeled,sliced cooking apples
      1 T  lemon juice
    1/2 c  sugar
    1/4 c  light brown sugar,packed
    1/4 c  sugar
    1/4 c  light brown sugar,packed
    1/3 c  butter,at room temperature
      3 T  all-purpose flour

Preheat oven to 375 degrees.

Brush bottom and sides of crust evenly with egg yolk;
bake on baking on baking sheet until light brown,about
5 minutes.Remove crust from oven.

Combine sliced apples,lemon juice,1/2 cup sugar,1/4 cup brown
sugar, 3 tablespoons flour,salt,cinnamon and nutmeg. Mix well
and spoon into crust. Mix remaining flour,sugar,brown sugar and
butter with fork until crumbly. Sprinkle topping mixture evenly
over apples.

Bake on baking sheet until topping is golden and filling is
bubbling; about 50 minutes.

Cool thoroughly on wire rack,at least 4 hours. Serve
at room temperature.

